import os
import requests
from dotenv import load_dotenv
from config.settings import (OPENAI_API_KEY, OPENAI_BASE_URL)

load_dotenv()

class LLMService:
    def __init__(self):

        self.api_key = OPENAI_API_KEY
        self.url = OPENAI_BASE_URL


    def generate_recommendation(self, suppliers, category):

        supplier_text = ""

        for supplier in suppliers:
            supplier_text += f"""
                Поставщик: {supplier['name']}
                Город: {supplier['city']}
                Рейтинг пользователей: {supplier['rating']}
                Минимальный заказ от {supplier['min_order']}
                Сертификаты: {supplier['certificates']}
                Бесплатная доставка от : {supplier['free_delivery']} рублей
                Условия оплаты: {supplier['payment_terms']}
                Уровень цен: {supplier['price_level']}
                Итоговый балл системы: {supplier['score']}
                """
        prompt = f"""
        Ты аналитик отдела закупок.
        Твоя задача — выбрать лучшего поставщика из представленных.
        Категория:
        {category}
        Поставщики:
        {supplier_text}
        ВАЖНО:
        Используй только предоставленные данные.
        Запрещено:
        - придумывать отзывы;
        - придумывать репутацию;
        - придумывать ассортимент;
        - придумывать риски, которых нет в данных;
        - использовать любой язык, кроме русского;
        - упоминать итоговый балл системы в ответе;
        - повторять один и тот же аргумент в разных разделах.
        Правила формирования ответа:
        1. Причины выбора должны содержать только критерии, по которым победитель превосходит конкурентов (меньший срок доставки, больший рейтинг пользователей, наличие сертификата,
        минимальная сумма заказа).
        2. Преимущества должны содержать только уникальные сильные стороны, которые ещё не были перечислены в причинах выбора.
        3. Риски должны содержать только реальные недостатки, которые присутствуют в данных поставщика.
        4. Если реальных рисков нет, напиши: "Существенных рисков по имеющимся данным не выявлено."
        5. Не упоминай регион как риск, если поставщик находится в выбранном пользователем регионе.
        6. Используй итоговый балл системы для итогового определения победителя, но не показывай балл пользователю.
        7. Если у фаворита минимальная сумма заказа выше, чем у конкурента, то это нужно записывать в "возможные недостатки", а не в "причины для выбора".
        8. При выводе информации о наличии сертификата избегай допольнительных комментариев по типу "(True у обоих, сравнение не требуется)".
        9. Если в сравнении участвует больше чем 1 конкурент, то упоминай и их в анализе. Не зацикливайся только на ближайшем конкуренте.
        10. Сравнивай итоговой был системы между конкурентами. Всегда делай выбор в пользу того, у кого рейтинг системы выше и говори об этом в заключении.
        11. Условие оплаты "Предоплата" менее выгодно для пользователей, чем "Отсрочка". Поэтому если у поставщика стоит "Отсрочка", то это надо указать в преимуществах, 
        а если "Предоплата" в возможных недостатках.
        12. Не упоминай регион других запросов. Действуй только в рамках одного запроса. 

        Верни ответ строго в формате:
        🏆 Рекомендуемый поставщик:
        ...

        📊 Причины для выбора:
        - ...
        - ...

        ✅ Преимущества:
        - ...
        - ...

        ⚠️ Возможные недостатки:
        - ...
        - ...

        📌 Итоговая рекомендация:
        "Поставщик <название лучшего поставщика> является более предпочтительным вариантом с учётом всех его преимуществ.

        """

        try:
        
            response = requests.post(
                self.url,
                headers={"Authorization": f"Bearer {self.api_key}", "Content-Type": "application/json"},
                json={"model": "gpt-4.1-mini", "messages": [{"role": "user", "content": prompt}]},
                timeout=30
            )
            response.raise_for_status()
            data = response.json()
            if "choices" not in data:raise Exception(f"Ошибка API: {data}")

            return data["choices"][0]["message"]["content"]

        except Exception as e:
        
            print(f"LLM ERROR: {e}")

            return """
        ⚠️ **В данный момент AI-рекомендация недоступна.**

        Возможные причины:
        - отсутствует подключение к интернету;
        - временно недоступен AI-сервис;
        - возникла ошибка авторизации API.

        Попробуйте выполнить анализ позже.
        """


